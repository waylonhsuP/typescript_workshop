// 原始型別 (Primitive Types) → Call by Value
// 基本、不可變、非物件的型別。直接儲存值本身。

// 數值型別：整數或浮點數（TypeScript 中統一為 number）
let n: number = 30;

// 字串型別：代表文字
let userName: string = "Alice";

// 布林型別：true 或 false
let isLoggedIn: boolean = true;

// 大整數：可儲存超過 JS 安全範圍的整數
let bigNumber: bigint = 123456789012345678901234567890n;

// Symbol：唯一值，用於建立物件的唯一屬性鍵
let uniqueKey: symbol = Symbol("id");

// undefined：變數尚未賦值
let notAssigned: undefined = undefined;

// null：明確指定變數為「空值」
let emptyValue: null = null;


